var visited=[]

function check(a,b)
{
  if(a<30 && b<30 && a>-1 && b>-1)
  {
    for(let i of visited)
    {
      if(i[0]==a && i[1]==b)
        return false;
    }
    return true;
  }
  return false;
}


function setup() {
    createCanvas(900,900);
    background(150);
}


async function draw() {
  for(let i=0;i<maze_map.length;i++)
  {
    for(let j=0;j<maze_map[i].length;j++)
    {
      if(maze_map[j][i])
      {
        fill(0);
        square(int(i*30),int(j*30),30);
      }
    }
  }
  
  
  let start=[0,0];
  let end=[29,29];
  
  var stack=[];
  stack.push(start);
  
  while(1)// noprotect
  {
      if(stack.length==0)
        break
        
      let curr=stack.pop();
    
      if(maze_map[curr[0]][curr[1]]==1)
      {
        continue;
      }
    
      if(curr in visited)
      {
        continue;
      }
      
      fill(255,0,0);
      square(curr[1]*30,curr[0]*30,30);
      visited.push(curr);
      
      maze_map[curr[0]][curr[1]]=2;
      await sleep(20);
      
      if(curr[0]==29 && curr[1]==29)
      {
        break;
      }
      fill(0,255,0);
      square(curr[1]*30,curr[0]*30,30);
    
      if(check(curr[0]-1,curr[1]))
        stack.push([curr[0]-1,curr[1]]);
    
      if(check(curr[0],curr[1]-1))
        stack.push([curr[0],curr[1]-1]);

      if(check(curr[0]+1,curr[1]))
        stack.push([curr[0]+1,curr[1]]);
    
      if(check(curr[0],curr[1]+1))
        stack.push([curr[0],curr[1]+1]);
      
      
  }
  print("DONE");
  noLoop();
}
